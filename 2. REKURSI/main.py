# 1. Most common style
print("Hello World")

# 2. With an exclamation mark (your version)
print("Hello world!")

# 3. With single quotes (also correct)
print('Hello World')

# 4. With a variable (very common when learning)
message = "Hello World"
print(message)

# 5. Python 3.6+ f-string style (modern & clean)
name = "Gilang"
print(f"Hello World from {name}!")